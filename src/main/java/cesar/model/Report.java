package cesar.model;

public class Report {
	private String type;
	private String fromQuarter;
	private String fromYear;
	private String toQuarter;
	private String toYear;
	private String option;
	private String year;
	private String group;
	private String group1;
	
	public Report() {
		
	}
	
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getFromQuarter() {
		return fromQuarter;
	}

	public void setFromQuarter(String fromQuarter) {
		this.fromQuarter = fromQuarter;
	}

	public String getFromYear() {
		return fromYear;
	}

	public void setFromYear(String fromYear) {
		this.fromYear = fromYear;
	}

	public String getToQuarter() {
		return toQuarter;
	}

	public void setToQuarter(String toQuarter) {
		this.toQuarter = toQuarter;
	}

	public String getToYear() {
		return toYear;
	}

	public void setToYear(String toYear) {
		this.toYear = toYear;
	}

	public String getOption() {
		return option;
	}

	public void setOption(String option) {
		this.option = option;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public String getGroup() {
		return group;
	}

	public void setGroup(String group) {
		this.group = group;
	}

	public String getGroup1() {
		return group1;
	}

	public void setGroup1(String group1) {
		this.group1 = group1;
	}

}
